<template>
    <div class="page-content">
        <p>
            <button v-on:click="list(1)" v-bind:list="list" class="btn btn-white btn-default btn-round">
                <i class="ace-icon fa fa-refresh green"></i>
                刷新
            </button>
        </p>

        <table id="simple-table" class="table  table-bordered table-hover">
            <thead>
            <tr>
                <th>id</th>
                <th>相对路径</th>
                <th>文件名</th>
                <th>后缀</th>
                <th>大小</th>
                <th>用途</th>
            </tr>
            </thead>

            <tbody>
            <tr v-for="file in files">
                <td>{{file.id}}</td>
                <td>{{file.path}}</td>
                <td>{{file.name}}</td>
                <td>{{file.suffix}}</td>
                <td>{{file.size | formatFileSize}}</td>
                <td>{{FILE_USE | optionKV(file.use)}}</td>
            </tr>

            </tbody>
        </table>

        <pagination ref="pagination" v-bind:list="list" v-bind:itemCount="8"></pagination>
    </div><!-- /.span -->
</template>

<script>
    import Pagination from "../../components/pagination";

    export default {
        components: {Pagination},
        name: "file-file",
        props: {
            text: {
                default: "上传文件"
            },
            inputId: {
                default: "file-upload"
            },
            suffixs: {
                default: []
            },
            use: {
                default: ""
            },
            afterUpload: {
                type: Function,
                default: null
            },
        },
        data: function () {
            return {
                file: {},
                files: [],
                FILE_USE: FILE_USE,
            }
        },
        mounted: function () {
            //激活侧边栏状态写法1
            // this.$parent.activeSidebar("file-file-sidebar");
            let _this = this;
            _this.$refs.pagination.size = 5;
            _this.list(1);
        },
        methods: {
            /**
             * 查询
             */
            list(page) {
                let _this = this;
                Loading.show();
                _this.$ajax.post(process.env.VUE_APP_SERVER + '/file/admin/file/list', {
                    page: page,
                    /*根据pagination名字获取组件*/
                    size: _this.$refs.pagination.size,
                })
                    .then((response) => {
                        Loading.hide();
                        /*接口返回的data是ChapterDto*/
                        let resp = response.data;
                        _this.files = resp.content.list;
                        _this.$refs.pagination.render(page, resp.content.total);
                    })
            },
            uploadFile(){
                let _this = this;
                //使用表单的方式提交图片
                let formData = new window.FormData();
                let file = _this.$refs.file.files[0];

                // 判断文件格式
                let suffixs = ["jpg","jpeg","png"];
                let fileName = file.name;
                let suffix = fileName.substring(fileName.lastIndexOf(".") + 1, fileName.length).toLowerCase();
                let validateSuffix = false;
                for (let i = 0; i < suffixs.length; i++) {
                    if (suffixs[i].toLowerCase() === suffix) {
                        validateSuffix = true;
                        break;
                    }
                }
                if (!validateSuffix) {
                    Toast.warning("文件格式不正确！只支持上传：" + suffixs.join(","));
                    $("#" + _this.inputId + "-input").val("");
                    return;
                }

                console.log(formData);
                console.log(document.querySelector('#file-upload-input').files[0]);
                //key file必须和后端controller参数名一致
                formData.append('file',file);
                formData.append('use', _this.use);
                Loading.show();
                _this.$ajax.post(process.env.VUE_APP_SERVER + '/file/admin/upload',formData).then((response)=>{
                    Loading.hide();
                    let resp = response.data;
                    console.log("上传文件成功：", resp);
                    _this.afterUpload(resp);
                    let image = resp.content;
                    $("#" + _this.inputId + "-input").val("");
                });
            },
            selectFile() {
                let _this = this;
                $("#" + _this.inputId + "-input").trigger("click");
            },
        }
    }
</script>

